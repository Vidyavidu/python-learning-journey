fruits = ["apple", "banana", "mango"]

# Modify element
fruits[1] = "orange"
print(fruits)

# Add element
fruits.append("grape")
print(fruits)

# Insert at position
fruits.insert(1, "kiwi")
print(fruits)

# Remove element
fruits.remove("apple")
print(fruits)

# Pop element
fruits.pop()
print(fruits)