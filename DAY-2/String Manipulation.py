text = "python programming"

# Uppercase
print(text.upper())

# Lowercase
print(text.lower())

# Capitalize
print(text.capitalize())

# Length of string
print(len(text))

# Access characters
print(text[0])
print(text[-1])

# Slicing
print(text[0:6])

# Replace
print(text.replace("python", "Java"))

# Check substring
print("python" in text)